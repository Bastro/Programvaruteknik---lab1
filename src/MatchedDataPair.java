
public class MatchedDataPair {
	private double xValue;
	private double yValue;
	
	public MatchedDataPair(double xValue, double yValue) {
		this.xValue = xValue;
		this.yValue = yValue;
	}

	public double getxValue() {
		return xValue;
	}

	public double getyValue() {
		return yValue;
	}
	
	@Override
	public String toString() {
		return "";
	}
}
